function s_vp_dateDifference(datefirst,datesecond) {

      returnval = "";

      diff = new Date();

      diff.setTime(Math.abs(datesecond.getTime() - datefirst.getTime()));

      timediff = diff.getTime();

      weeks = Math.floor(timediff / (1000 * 60 * 60 * 24 * 7));

      timediff -= weeks * (1000 * 60 * 60 * 24 * 7);

      returnval+=weeks + "w ";

      days = Math.floor(timediff / (1000 * 60 * 60 * 24)); 

      timediff -= days * (1000 * 60 * 60 * 24);

      returnval+=days + "d";

      if(days<1) {

            hours = Math.floor(timediff / (1000 * 60 * 60)); 

            timediff -= hours * (1000 * 60 * 60);

            if(hours < 1) 

                  returnval="< 1 hour";

            else { if (hours ==1) returnval="1 hour";

                        else returnval=hours+" hours";

            }

      }

      return returnval;

}

 

