function s_AddSuccessEvent(args)
{
    args = args.split(',');
    result = s.events;
    for(i=0; i<args.length; i++)
    {
        if(args[i].length >0)
            if(result != undefined)
            {
                var matchingStr = ',' + result + ',';
                if(matchingStr.search(',' + args[i] + ',') == -1)
                    // if new event not in s.events then add it
                    if(result.length > 0)
                        result += ',' + String(args[i]);
                    else
                        result = String(args[i]);
            }
            else
                result = String(args[i]);
    }
    return result;
}

